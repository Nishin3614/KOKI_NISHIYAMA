{
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 初期化
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CTitle::Init(void)
{
	/* 作成 */
	// 背景
	/* 作成 */
	// 3Dエフェクトの生成
	C3DEffect::Create();
	// 軌跡の生成
	CMeshobit::Create();
	// 球の生成
	CMeshsphere::Create();
	// 球の設定
	CMeshsphere::Set(D3DXVECTOR3(0.0f, 0.0f, 0.0f),
		5000.0f);
	// 壁の生成
	CMeshwall::LoadCreate();
	// フロアー
	CFloor::Create(
		D3DXVECTOR3(0.0f, 0.0f, 0.0f),
		D3DXVECTOR3(1000.0f, 0.0f, 1000.0f),
		D3DXVECTOR3(0.0f, 0.0f, 0.0f),
		3, 3);
	// プレイヤー
	CPlayer::Create();
	// UI
	CTitle_ui::Create();
	// カウント遷移
	m_nCntChange = 0;
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 終了
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CTitle::Uninit(void)
{
	CScene::ReleaseAll();
}

// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// 更新
// ----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void CTitle::Update(void)
{
	// カウント遷移
	m_nCntChange++;
	// ランキング遷移
	if (m_nCntChange >= INTERVAL_TIME)
	{
		// 画面遷移の状態が遷移していない状態だったら
		if (CManager::GetFade()->GetFade() == CFade::FADE_NONE)
		{		
			CManager::GetFade()->SetFade(CManager::MODE_RANKING);
		}
	}
	// 選択画面遷移
	else if (CManager::GetKeyboard()->GetKeyboardTrigger(DIK_RETURN))
	{
		// 画面遷移の状態が遷移していない状態だったら
		if (CManager::GetFade()->GetFade() == CFade::FADE_NONE)
		{
			CManager::GetFade()->SetFade(CManager::MODE_TUTORIAL);
			// エンター音
			CManager::GetSound()->PlaySound(CSound::LABEL_SE_ENTER);
		}
	}
	// 選択画面遷移
	else if (CManager::GetJoy() != NULL)
	{
		if (CManager::GetJoy()->GetTrigger(0, CJoypad::KEY_START) ||
			CManager::GetJoy()->GetTrigger(0, CJoypad::KEY_B) ||
			CManager::GetJoy()->GetTrigger(0, CJoypad::KEY_A))
		{
			// 画面遷移の状態が遷移していない状態だったら
			if (CManager::GetFade()->GetFade() == CFade::FADE_NONE)
			{
				CManager::GetFade()->SetFade(CManager::MODE_TUTORIAL);
				// エンター音
				CManager::GetSound()->PlaySound(CSound::LABEL_SE_ENTER);
			}
		}
	}
}

